	public DURATION(Clazz.Duration duration) {
		durationList = new ArrayList<Clazz.Duration>();
		durationList.add(duration);
	}

	@Override
	public Clazz.Duration getDuration() {
		if(durationList != null && durationList.size() > 0) {
			return durationList.get(0);
		} else {
			return null;
		}
	}

	@Override
	public void setDuration(Clazz.Duration duration) {
		if(durationList == null) {
			durationList = new ArrayList<>();
		}
		if(durationList.size() == 0) {
			durationList.add(duration);
		} else {
			durationList.set(0, duration);
		}
	}

	@Override
	public List<Clazz.Duration> getDurationList() {
		return durationList;
	}

	@Override
	public void setDurationList(List<Clazz.Duration> durationList) {
		this.durationList = durationList;
	}

	@Override
	public boolean hasDuration() {
		return durationList != null && durationList.size() > 0 && durationList.get(0) != null;
	}

